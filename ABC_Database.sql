CREATE DEFINER=`root`@`localhost` PROCEDURE `ReadLastMonthProgress`()
BEGIN
SELECT 
        STR_TO_DATE(P.payment_date, '%Y-%m-%d') AS payment_day,
        IFNULL(SUM(S.rate), 0) AS daily_total_income
    FROM 
        payments P 
    JOIN 
        reservations R ON P.reservation_id = R.reservation_id
    JOIN 
        services S ON R.service_id = S.service_id
    WHERE
        P.payment_status = 1
        AND MONTH(STR_TO_DATE(P.payment_date, '%Y-%m-%d')) = MONTH(CURRENT_DATE - INTERVAL 1 MONTH)
        AND YEAR(STR_TO_DATE(P.payment_date, '%Y-%m-%d')) = YEAR(CURRENT_DATE)
    GROUP BY 
        STR_TO_DATE(P.payment_date, '%Y-%m-%d')
    ORDER BY 
        STR_TO_DATE(P.payment_date, '%Y-%m-%d');
END ;;
