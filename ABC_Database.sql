CREATE DEFINER=`root`@`localhost` PROCEDURE `ReadDailySummary`()
BEGIN
-- Declare variables for storing results
    DECLARE total_income_today DECIMAL(10, 2) DEFAULT 0;
    DECLARE total_customers INT DEFAULT 0;
    DECLARE total_reservations_today INT DEFAULT 0;
    DECLARE total_paid_reservations_today INT DEFAULT 0;
    
    -- Total income for today
    SELECT IFNULL(SUM(S.rate), 0) INTO  total_income_today
    FROM payments P
    JOIN reservations R ON P.reservation_id = R.reservation_id
    JOIN services S ON R.service_id = S.service_id
    WHERE DATE(P.payment_date) = CURRENT_DATE()
      AND P.payment_status = 1;

    -- Total number of customers registered today
    SELECT COUNT(user_id) INTO  total_customers
    FROM users;

    -- Total number of reservations for today
    SELECT COUNT(R.reservation_id) INTO  total_reservations_today
    FROM reservations R
    WHERE DATE(R.reservation_date) = CURRENT_DATE();

    -- Total number of paid reservations for today
    SELECT COUNT(P.reservation_id) INTO  total_paid_reservations_today
    FROM payments P
    JOIN reservations R ON P.reservation_id = R.reservation_id
    WHERE DATE(P.payment_date) = CURRENT_DATE()
      AND P.payment_status = 1;
      
      -- Return all the results
    SELECT 
        total_income_today ,
        total_customers,
        total_reservations_today,
        total_paid_reservations_today;
END ;;
